package com.floyd.core.callback;

public enum ErrorCode {

	TRANSFORMER_ERROR(1, "");

	private int code;
	private String info;

	ErrorCode(int code, String info) {
		this.code = code;
		this.info = info;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

}
